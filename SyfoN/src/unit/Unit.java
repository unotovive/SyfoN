package unit;

public class Unit {
	private String unitID;
	private String unitName;
	private int unitTani;

	public String getUnitID() {
		return unitID;
	}
	public void setUnitID(String unitID) {
		this.unitID = unitID;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public int getUnitTani() {
		return unitTani;
	}
	public void setUnitTani(int unitTani) {
		this.unitTani = unitTani;
	}

}
